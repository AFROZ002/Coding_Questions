package Star_Pattern_Coding_Problem;

public class Pattern_Counting11 {
    public static void main(String[] args) {
        for(int i=0;i<=5;++i){
            for(int j=1;j<=i;++j){
                System.out.print(j);
//                System.out.print(i);
            }
            System.out.println();
        }
    }
}
